# ⭐️ Lambda Functions ⭐️
# A lambda function is a small anonymous function.
# Anonymous function vanako tyo function jasko name xaina , body matra xa.

# lambda function ko syntax:
# lambda arguments : expression

add=lambda x,y:x+y
print(add(2,3))

print("--"*20)

sum=lambda x:x+10
print(sum(2))

print("--"*20)

x= lambda a,b,c: a+b-c
print(x(2,3,4))

